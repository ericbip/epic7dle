function populateAutocomplete() {
    const container = document.getElementById('autocomplete-list');
    container.innerHTML = '';
    
    heroes.filter(hero => 
        !guessedHeroes.includes(hero.name.toLowerCase())
    ).forEach(hero => {
        const div = document.createElement('div');
        div.className = 'autocomplete-item';
        
        const imgName = hero.name.toLowerCase().replace(/ /g, '_') + '.png';
        div.innerHTML = `
            <img src="images/characters/${imgName}" 
                 alt="${hero.name}"
                 onerror="this.style.display='none'">
            <span>${hero.name}</span>
        `;
        
        div.addEventListener('click', () => {
            document.getElementById('guess-input').value = hero.name;
            container.style.display = 'none';
        });
        
        container.appendChild(div);
    });
}

