function getDailyHero() {
    const today = new Date();
    const seed = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
    
    const storedTodayHero = localStorage.getItem('todayHero');
    if (storedTodayHero !== seed) {
        const previousHero = localStorage.getItem('todayHeroName');
        if (previousHero) {
            localStorage.setItem('yesterdayHero', previousHero);
            yesterdayHero = previousHero;
            document.getElementById('yesterday-champion').textContent = 
                `Yesterday's champion was: ${yesterdayHero}`;
        }
        localStorage.setItem('todayHero', seed);
    }
    
    if (lastPlayedDate) {
        const lastDate = new Date(lastPlayedDate);
        const todayReset = new Date().setHours(0,0,0,0);
        const isNewDay = lastDate < todayReset;
        
        if (isNewDay) {
            const isConsecutive = (todayReset - lastDate) <= 86400000;
            if (!isConsecutive) {
                currentStreak = 0;
                localStorage.setItem('currentStreak', currentStreak);
            }
        }
    }
    
    Math.seedrandom(seed);
    const dailyHero = heroes[Math.floor(Math.random() * heroes.length)];
    localStorage.setItem('todayHeroName', dailyHero.name);
    return dailyHero;
}

let targetHero = getDailyHero();

function submitGuess() {
    const guessInput = document.getElementById("guess-input");
    const guess = guessInput.value.trim();
    
    if (!guess) return;

    const hero = heroes.find(h => 
        h.name.toLowerCase() === guess.toLowerCase()
    );

    if (!hero) {
        alert("¡Héroe no encontrado! Usa la lista de autocompletado.");
        return;
    }

    guessedHeroes.push(hero.name.toLowerCase());
    populateAutocomplete();
    
    const isCorrect = hero.name === targetHero.name;
    revealClues(hero, isCorrect);
    
    if (isCorrect) {
        endGame(true);
    }
    
    guessInput.value = '';
}

function revealClues(hero) {
    const grid = document.getElementById("grid");
    const row = document.createElement("div");
    row.className = "grid-row";

    const imageName = hero.name.toLowerCase()
        .replace(/ /g, '_')
        .replace(/[^a-z0-9_]/g, '') + '.png';

    const properties = [
        { 
            type: 'image', 
            value: `images/characters/${imageName}`,
            alt: hero.name
        },
        { 
            type: 'text',
            value: `${hero.rarity}★`, 
            target: `${targetHero.rarity}★` 
        },
        { 
            type: 'role',
            value: hero.role.toLowerCase(), 
            target: targetHero.role.toLowerCase() 
        },
        { 
            type: 'element',
            value: hero.attribute.toLowerCase(), 
            target: targetHero.attribute.toLowerCase() 
        },
        { 
            type: 'zodiac',
            value: hero.zodiac.toLowerCase(), 
            target: targetHero.zodiac.toLowerCase() 
        },
        { 
            type: 'text',
            value: hero.sex, 
            target: targetHero.sex 
        }
    ];

    properties.forEach(prop => {
        const cell = document.createElement("div");
        cell.className = "grid-cell";
        
        switch(prop.type) {
            case 'image':
				const imgContainer = document.createElement('div');
				const img = document.createElement('img');
				img.src = prop.value;
				img.alt = prop.alt;
				img.className = 'character-image';
				
				// Eliminar cualquier estilo de borde o fondo
				img.style.border = 'none';
				img.style.background = 'transparent';
				
				img.onerror = () => {
					img.style.display = 'none';
					const fallback = document.createElement('div');
					fallback.textContent = prop.alt;
					fallback.style.color = '#e67e22';
					imgContainer.appendChild(fallback);
				};
				imgContainer.appendChild(img);
				cell.appendChild(imgContainer);
				
				// Evitar que herede estilos de otras celdas
				cell.style.background = 'transparent';
				cell.style.border = 'none';
				break;
                
            case 'element':
				cell.setAttribute('data-tooltip', prop.value.charAt(0).toUpperCase() + prop.value.slice(1));
				cell.className += ' icon-cell';
				cell.setAttribute('data-element', prop.value);
				const elementImg = document.createElement('img');
				elementImg.src = elementImages[prop.value];
				elementImg.className = 'element-icon';
				elementImg.title = prop.value.charAt(0).toUpperCase() + prop.value.slice(1); // Tooltip
				elementImg.onerror = () => elementImg.style.display = 'none';
				cell.appendChild(elementImg);
				break;
                
            case 'role':
				cell.setAttribute('data-tooltip', prop.value.charAt(0).toUpperCase() + prop.value.slice(1));
				cell.className += ' icon-cell';
				cell.setAttribute('data-role', prop.value);
				const roleImg = document.createElement('img');
				roleImg.src = roleImages[prop.value];
				roleImg.className = 'role-icon';
				roleImg.title = prop.value.charAt(0).toUpperCase() + prop.value.slice(1); // Tooltip
				roleImg.onerror = () => roleImg.style.display = 'none';
				cell.appendChild(roleImg);
				break;

			case 'zodiac':
				cell.setAttribute('data-tooltip', prop.value.charAt(0).toUpperCase() + prop.value.slice(1));
				cell.className += ' icon-cell';
				const zodiacImg = document.createElement('img');
				zodiacImg.src = zodiacImages[prop.value];
				zodiacImg.className = 'zodiac-icon';
				zodiacImg.title = prop.value.charAt(0).toUpperCase() + prop.value.slice(1); // Tooltip
				zodiacImg.onerror = () => zodiacImg.style.display = 'none';
				cell.appendChild(zodiacImg);
				break;
                
            default:
                cell.textContent = prop.value;
        }

        if (prop.value === prop.target) {
			cell.classList.add('correct-bg');
		} else {
			cell.classList.add('incorrect-bg');
		}
        
        row.appendChild(cell);
    });

    grid.appendChild(row);
}

function endGame(isWinner) {
    const result = document.getElementById("result");
    result.innerHTML = isWinner ? 
        `<div class="correct-message">🎉 ¡Correcto! ${targetHero.name}</div>` :
        `<div class="incorrect-message">😢 ¡El héroe era: ${targetHero.name}</div>`;
    
    document.getElementById("guess-input").disabled = true;
    document.querySelector('button').disabled = true;

    if (isWinner) {
        const today = new Date();
        localStorage.setItem('lastPlayedDate', today);
        currentStreak++;
        localStorage.setItem('currentStreak', currentStreak);
        updateStreakDisplay();
    }

    if (yesterdayHero) {
        document.getElementById('yesterday-champion').textContent = 
            `Yesterday's champion was: ${yesterdayHero}`;
    }
}

